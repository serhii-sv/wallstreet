<script type="text/javascript">
    window.onload = function () {
        var canvas = document.getElementById('canvas');
        canvas.width = document.body.clientWidth;
        canvas.height = document.body.clientHeight;

        var ctx = canvas.getContext('2d');

        // Snow Particles

        var maxParticles = 500;
        var particles = [];

        for (var i = 0; i < maxParticles; i++) {
            // add new particle to end of array
            particles.push({
                x: Math.random() * canvas.width,
                y: Math.random() * canvas.height,
                r: Math.random() * 3,
                d: Math.random() * maxParticles

            })
        }


        // Drawing the particles to the canvas
        function draw() {
            ctx.clearRect(0, 0, canvas.width, canvas.height);

            ctx.fillStyle = ('white');
            ctx.beginPath();
            for (var i = 0; i < maxParticles; i++) {
                var p = particles[i];
                ctx.moveTo(p.x, p.y);
                ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2, true);
            }

            ctx.fill();

            update();

        }

        //  Moving the particles function
        var angle = 0;

        function update() {
            angle += 0.01;
            for (var i = 0; i < maxParticles; i++) {
                var p = particles[i];
                //Updating X and Y coordinates
                p.x += Math.sin(angle) * 2;
                //We will add 1 to the cos f
                p.y += Math.cos(angle + p.d) + 1 + p.r;


                if (p.x > canvas.width || p.x < 0 || p.y > canvas.height) {
                    particles[i] = {x: Math.random() * canvas.width, y: -10, r: p.r, d: p.d}
                }
            }

        }


        setInterval(draw, 30);

    }        </script>

